    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Random sorte = new Random();
       Integer porcentagem = sorte.nextInt(100);         
       cpuLab.setText(porcentagem+"%");
       cpuBarra.setValue(porcentagem);
       
       
     // Integer CPU = cpuLab.getText
       Integer Barra= cpuBarra.getValue();
       
       if (Barra >=60 && Barra<80) { 
          cpuLab.setForeground(Color.YELLOW);
       } else if (Barra >=80){
           cpuLab.setForeground(Color.RED);
       } else { 
           cpuLab.setForeground(Color.GREEN);
       }
           
       
       
       
       Random sorte2 = new Random();
       Integer porcentagem2 = sorte2.nextInt(100);         
       memoriaLab.setText(porcentagem2+"%");
       memoriaBarra.setValue(porcentagem2);
       
        Integer Barra2= memoriaBarra.getValue();
       
       if (Barra2 >=60 && Barra2<80) { 
          memoriaLab.setForeground(Color.YELLOW);
       } else if (Barra2 >=80){
           memoriaLab.setForeground(Color.RED);
       } else { 
           memoriaLab.setForeground(Color.GREEN);
       }
       
       
       Random sorte3 = new Random();
       Integer porcentagem3 = sorte3.nextInt(100);         
       discoLab.setText(porcentagem3+"%");
       discoBarra.setValue(porcentagem3);
       
        Integer Barra3= discoBarra.getValue();
       
       if (Barra3 >=60 && Barra3<80) { 
          discoLab.setForeground(Color.YELLOW);
       } else if (Barra3 >=80){
          discoLab.setForeground(Color.RED);
       } else { 
          discoLab.setForeground(Color.GREEN);
       }
       
       
       if ( Barra >=80 && Barra2 >= 80 && Barra3 >= 80){
           rede.setText("Desconectado da Rede");
                   rede.setForeground(Color.red);
       } else {
           rede.setText("conectado em Rede");
           rede.setForeground(Color.GREEN);
       }
       
       if (redebox.isSelected()){
           redebox.setForeground(Color.GREEN);
       } else {
           redebox.setForeground(Color.red);
       }
       
       
    }//GEN-LAST:event_jButton1ActionPerformed


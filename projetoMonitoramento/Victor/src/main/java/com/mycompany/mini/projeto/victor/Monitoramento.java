    private void bAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtualizarActionPerformed

       Integer cpu = aleatorio.nextInt(101);
       Integer disco = aleatorio.nextInt(101);
       Integer memoria = aleatorio.nextInt(101);
       Integer rede = aleatorio.nextInt(101);
        
       pPorcentagem1.setValue(cpu);
       valor1.setText(cpu+"%");
       
       pPorcentagem2.setValue(disco);
       valor2.setText(disco+"%");
       
       pPorcentagem3.setValue(memoria);
       valor3.setText(memoria+"%");
       
       //monitoramento da cpu
       if(cpu < 50){
           valor1.setForeground(Color.green);
       }
       else if(cpu > 80){
           valor1.setForeground(Color.red);
       }
       else{
           valor1.setForeground(Color.orange);
       }
       
       //monitoramento do disco
       if(disco < 50){
           valor2.setForeground(Color.green);
       }
       else if(disco > 80){
           valor2.setForeground(Color.red);
       }
       else{
           valor2.setForeground(Color.orange);
       }
       
       //monitoramento da memoria
       if(memoria < 50){
           valor3.setForeground(Color.green);
       }
       else if(memoria > 80){
           valor3.setForeground(Color.red);
       }
       else{
           valor3.setForeground(Color.orange);
       }
       
       //monitoramento da rede
       if(rede >= 50){
           lbRede.setForeground(Color.green);
           lbRede.setText("Online");
       }
       else{
           lbRede.setForeground(Color.red);
           lbRede.setText("Offline");
       }
    }//GEN-LAST:event_bAtualizarActionPerformed

